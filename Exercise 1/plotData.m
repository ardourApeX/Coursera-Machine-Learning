function plotData(x, y) % Function Definition
  %This function takes 2 argument for its invocation
  figure; % open a new figure window
  plot(x,y,'rx','MarkerSize',10); % Intializing figure window with x & Y with markersize and symbol size of 10
  xlabel("X"); % Labeling X axis
  ylabel("Y"); % Labeling Y axis
endfunction
